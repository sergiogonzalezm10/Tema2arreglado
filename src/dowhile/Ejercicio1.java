package dowhile;

public class Ejercicio1 {

	public static void main(String[] args) {
		
		//Variable para almacenar el valor del número"
		int num = 1; 
		
		//Bucle do-while para imprimir los números del 1 al 20
		do {
            System.out.println(num); 
            num++; 
      } while (num <= 20); 
		
    }
}